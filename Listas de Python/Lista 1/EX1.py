#Primeiro elemento para fazer a conta
x = int(input())

#Pega o segundo elemento para fazer a conta
y = int(input())

#Soma
print(x + y)

#Sub do primeiro pelo segundo
print(x - y)

#sub do segundo pelo primeiro
print(y - x)

#Mult de dois números
print(x*y)

#div do primeiro pelo segundo
print(x / y)

#div do segundo pelo primeiro
print(y / x)

#quociente da divisão do primeiro pelo segundo
print(x // y)

#quociente inteiro da divisão do primeiro pelo segundo
print(y // x)

#resto da div do primeiro pelo segundo
print(x % y)

#resto da div do segundo pelo primeiro
print(y % x)

#soma de todas as anteriores
print((x + y) + (x - y) + (y - x) + (x*y) + (x / y) + (y / x) + (x // y) + (y // x) + (x % y) + (y % x))